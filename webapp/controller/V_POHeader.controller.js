				oModel = this.getOwnerComponent().getModel(),
				oTable = this.getView().byId("poHeaderTableId"),
				oFilters = [];
			// oModel.addBatchChangeOperations(batchChanges);
			oModel.setUseBatch(false);
			this.getView().setModel(dataModel, "dataModel");
